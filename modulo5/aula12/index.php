<?php

    for($i = 0; $i < 10; $i++){
        echo 'Olá'.$i;
        echo '<hr/>';
    }

    // laço de repetiçao while
    $ii = 1;
    while($ii < 10){
        echo 'OLáa'.$ii;
        echo '<hr/>';
        $ii++;
    }

    //laço de repetição 'do'
    $iii = 0;
    do{
        echo 'ola mundo';
        echo '<br/>';
        $iii++;
    }while($iii < 10);


?>